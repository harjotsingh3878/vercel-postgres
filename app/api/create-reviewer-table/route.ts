import { sql } from '@vercel/postgres';
import { NextResponse } from 'next/server';
 
export async function GET(request: Request) {
  try {
    const result =
      await sql`CREATE TABLE Reviewers ( name varchar(255), company varchar(255), job_title varchar(255), street_address varchar(255), province varchar(255), city varchar(255), mobile varchar(255), email varchar(255) );`;
    return NextResponse.json({ result }, { status: 200 });
  } catch (error) {
    return NextResponse.json({ error }, { status: 500 });
  }
}